/**
 * цикл for
 * услови ()
 * тело цикла в {}
 */


public class alishev5for {
    public static void main(String[] args) {
        //переменна, затем условие, затем действие у нас переменна +
        for (int i = 0; i <= 10; i= i+5 ){
            System.out.println("Hello " + i);
        }
    }
}
