package com.nevamefa.cs.lesson7;

public class Lesson7Runner {

    public static void main(String[] args) {
        System.out.println(25); //int
        System.out.println(25L); //long

        System.out.println(23.879); //double (вещественный, дробные числа)
        System.out.println(23.879F); // float

        System.out.println('?'); //char у символа ''
        System.out.println("?,."); //string у строки ""

        System.out.println(false); //boolean ключевые слова оранжевые

    }
}
